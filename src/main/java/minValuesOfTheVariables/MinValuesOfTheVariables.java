package minValuesOfTheVariables;

public class MinValuesOfTheVariables {

    public static void main(String[] args) {
        byte minByteValue = Byte.MIN_VALUE;
        short minShortValue = Short.MIN_VALUE;
        int minIntegerValue = Integer.MIN_VALUE;
        long minLongValue = Long.MIN_VALUE;
        float minFloatValue = Float.MIN_VALUE;
        double minDoubleValue = Double.MIN_VALUE;

        System.out.println("Min byte value is " + minByteValue);
        System.out.println("Min short value is " + minShortValue);
        System.out.println("Min integer value is " + minIntegerValue);
        System.out.println("Mint long value is " + minLongValue);
        System.out.println("Min float value is " + minFloatValue);
        System.out.println("Min double value is " + minDoubleValue);
    }
}
